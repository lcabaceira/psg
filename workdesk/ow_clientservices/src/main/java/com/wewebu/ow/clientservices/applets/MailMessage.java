public class MailMessage
{

    private java.util.List<String> toAddrs;

    private java.util.List<String> ccAddrs;

    private java.util.List<String> bccAddrs;

    private java.lang.String subject;

    private java.lang.String body;

    private java.util.List<String> attachments = new ArrayList<String>();

    public java.util.List<?> getToAddrs()
    {
        return toAddrs;
    }

    public void setToAddrs(java.util.List<String> toAddrs)
    {
        this.toAddrs = toAddrs;
    }

    public java.util.List<?> getCcAddrs()
    {
        return ccAddrs;
    }

    public void setCcAddrs(java.util.List<String> ccAddrs)
    {
        this.ccAddrs = ccAddrs;
    }

    public java.util.List<String> getBccAddrs()
    {
        return bccAddrs;
    }

    public void setBccAddrs(java.util.List<String> bccAddrs)
    {
        this.bccAddrs = bccAddrs;
    }

    public java.lang.String getSubject()
    {
        return subject;
    }

    public void setSubject(java.lang.String subject)
    {
        this.subject = subject;
    }

    public java.lang.String getBody()
    {
        return body;
    }

    public void setBody(java.lang.String body)
    {
        this.body = body;
    }

    public java.util.List<String> getAttachments()
    {
        return attachments;
    }

    public void setAttachments(java.util.List<String> attachments)
    {
        this.attachments = attachments;
    }

}
