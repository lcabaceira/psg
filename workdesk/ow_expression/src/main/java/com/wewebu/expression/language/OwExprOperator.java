public abstract class OwExprOperator
{
    private String m_image;

    /**
     * Constructor
     * @param image_p literal operator image (egg. <code>"+"</code>,<code>"&lt;="</code>)
     */
    public OwExprOperator(String image_p)
    {
        super();
        this.m_image = image_p;
    }

    public String toString()
    {
        return m_image;
    }
}
